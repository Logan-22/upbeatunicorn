import axios from "axios";
import { setAlert } from "../actions/alert";
import {
  GET_PROFILE,
  PROFILE_ERROR,
  UPDATE_PROFILE,
  CLEAR_PROFILE,
  ACCOUNT_DELETED,
} from "../actions/types";

//*Get Current Users Profile

export const getCurrentProfile = () => async (dispatch) => {
  try {
    const res = await axios.get("api/profile/me");
    dispatch({
      type: GET_PROFILE,
      payload: res.data,
    });
  } catch (err) {
    dispatch({
      type: PROFILE_ERROR,
      payload: {
        msg: err.response.statusText,
        status: err.response.status,
      },
    });
  }
};

//* Create or Update Profile ..

export const createProfile = (formData, history, edit = false) => async (
  dispatch
) => {
  //! history object to redirect user to client side route after submitting profile and a edit parameter to indicate whether it is to create/update profile
  try {
    const config = {
      headers: {
        "Content-Type": "application/json",
      },
    };
    const res = await axios.post("/api/profile", formData, config);
    dispatch({
      type: GET_PROFILE,
      payload: res.data,
    });

    //?If Editing profile, stay on the same page , if creating profile redirect to /dashboard
    if (!edit) {
      history.push("/dashboard"); //! we can't use <Redirect..> in actions file,so we have to pass in the history object which has the push method..
    }
    dispatch(setAlert(edit ? "Profile Updated" : "Profile Created", "success"));
  } catch (err) {
    const errors = err.response.data.errors;
    if (errors) {
      errors.forEach((error) => dispatch(setAlert(error.msg, "danger")));
    }
    dispatch({
      type: PROFILE_ERROR,
      payload: {
        msg: err.response.statusText,
        status: err.response.status,
      },
    });
  }
};

